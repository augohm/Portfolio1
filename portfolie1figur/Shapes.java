// Denne fil vil håndtere nogle af de gennemgående funktionaliteter som bruges ved de1 forskellige former. Dette er blandt andet smart at have hvis man senere ville tilføje nye figure.

public abstract class Shapes {
  public abstract double centerX();
  public abstract double centerY();

  public abstract double circumference();
  public abstract double area(); 

 public abstract boolean isPointInsideShape();
}

